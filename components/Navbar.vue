<template>
  <div>
    <v-app-bar flat>
      <v-icon class="navbar-menu-icon" @click="drawer = !drawer"
        >mdi-menu</v-icon
      >
      <h2 class="navbar-title">BB LABO.</h2>
      <v-btn small outlined tile class="navbar-reservation-button"
        >Web予約</v-btn
      >
    </v-app-bar>
  </div>
</template>

<script lang="ts">
import { Component, Vue } from 'vue-property-decorator'

@Component
export default class Navbar extends Vue {
  private drawer: boolean = false

  private items = [
    { title: 'Home', icon: 'mdi-home' },
    { title: 'Programs', icon: 'mdi-dumbbell' },
    { title: 'Trainers', icon: 'mdi-account-multiple' },
    { title: 'Interview', icon: 'mdi-forum' },
    { title: 'Location', icon: 'mdi-google-maps' },
  ]

  private logoTextWhite = require('../assets/images/bb_logo_text_white.svg')
  private logoText = require('../assets/images/bb_logo_text.svg')

  private scrollY = 0

  private windowHeight = 0

  private isClient = process.client

  created() {
    if (this.isClient) {
      window.addEventListener('scroll', this.handleScroll)
      this.handleScroll()
      window.addEventListener('resize', this.handleResize)
      this.handleResize()
    }
  }

  destroyed() {
    if (this.isClient) {
      window.addEventListener('scroll', this.handleScroll)
    }
  }

  private handleScroll() {
    if (this.isClient) {
      this.scrollY = window.scrollY
    }
  }

  handleResize() {
    this.windowHeight = window.innerHeight
  }

  private isInsideOfFirstview() {
    if (this.scrollY < this.windowHeight - 30) {
      return true
    }
  }
}
</script>

